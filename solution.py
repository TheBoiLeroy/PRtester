def add_numbers(a: float, b: float) -> float:
    """
    Adds two numbers and returns the result.

    Parameters:
    a (float): The first number.
    b (float): The second number.

    Returns:
    float: The sum of the two numbers.

    Example:
    >>> add_numbers(2, 3)
    5.0
    """
    return a + b

